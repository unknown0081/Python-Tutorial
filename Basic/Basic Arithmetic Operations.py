def main():
    foo = 1
    bar = 2

    print("foo + bar:", foo + bar)  # 1 + 2 = 3
    print("foo - bar:", foo - bar)  # 1 - 2 = -1
    print("foo * bar:", foo * bar)  # 1 x 2 = 2
    print("foo / bar:", foo / bar)  # 1 ÷ 2 = 0.5
    print("foo % bar:", foo % bar)  # 1 あまり 2 = 1


if __name__ == "__main__":
    main()
