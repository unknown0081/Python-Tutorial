def main():
    # 変数の代入
    intVar = 1
    floatVar = 1.0
    strVar = "Hello, World!"
    boolVar = True

    # 変数の型を出力
    print("整数      :", intVar)
    print("実数      :", floatVar)
    print("文字列    :", strVar)
    print("ブーリアン:", boolVar)

    # 変数の演算
    intVar += 1
    intVar = intVar + 1
    print("整数      :", intVar)


if __name__ == "__main__":
    main()
